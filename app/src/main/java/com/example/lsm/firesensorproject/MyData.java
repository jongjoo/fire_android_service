package com.example.lsm.firesensorproject;

public class MyData {
    private String name;
    private String phone_number;

    public MyData(String name, String phone_number) {
        this.name = name;
        this.phone_number = phone_number;
    }

    public String getName() {
        return name;
    }

    public String getPhoneNumber() {
        return phone_number;
    }
}